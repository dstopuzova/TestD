public class zad5 {
    public static  void main(String[] arg) {

        for (int i = 1; i <= 100; i++) {
            System.out.println(i); //print numbers from 1 to 100 asc
        }

        for (int j = 100; j >= 1; j--) {
            System.out.println(j); //print numbers from 1 to 100 desc
        }
    }
}
